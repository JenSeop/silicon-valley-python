# Dynamic Typing
# Dynamic Type vs. Static Type vs Strong typing
# 파이썬의 자료형은 변수의 타입이 가변

# Dynamic typing means that runtime objects (values) have a type,
# as opposed to static typing where variables have a type.

# Strong typing means that variables do have a type and that
# the type matters when performing operations on a variable.

# print(1 + "a")

a = 1
print(type(a))

a = "A"
print(type(a))

